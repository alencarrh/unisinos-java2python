class ForExemplo:

    def metodo1(a = None, b = None): # returns VOID

        i = a
        while (i<b):
            i += 1
            print(i)

    def metodo2(): # returns VOID

        i = 0
        while (i<10):
            i += 1

            j = 0
            while (j<10):
                j += 1
                print(i+j)

